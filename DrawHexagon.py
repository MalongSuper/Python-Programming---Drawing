# Draw a Hexagon
import turtle
# Move the pen
turtle.penup()
turtle.goto(-150, 25)
turtle.pendown()
# Drawing
turtle.left(55)
turtle.forward(120)
turtle.right(55)
turtle.forward(150)
turtle.right(55)
turtle.forward(120)
turtle.right(70)
turtle.forward(120)
turtle.right(55)
turtle.forward(150)
turtle.right(55)
turtle.forward(120)
turtle.hideturtle()

turtle.done()
